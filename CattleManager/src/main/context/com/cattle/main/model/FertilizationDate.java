package com.cattle.main.model;

public class FertilizationDate {
	private String fd_seq; 
	private String fd_reg_date; 
	private String fi_seq; 
	private String member_seq;
	
	public FertilizationDate(){}
	
	public String getFd_seq() {
		return fd_seq;
	}
	public void setFd_seq(String fd_seq) {
		this.fd_seq = fd_seq;
	}
	public String getFd_reg_date() {
		return fd_reg_date;
	}
	public void setFd_reg_date(String fd_reg_date) {
		this.fd_reg_date = fd_reg_date;
	}
	public String getFi_seq() {
		return fi_seq;
	}
	public void setFi_seq(String fi_seq) {
		this.fi_seq = fi_seq;
	}
	public String getMember_seq() {
		return member_seq;
	}
	public void setMember_seq(String member_seq) {
		this.member_seq = member_seq;
	}
	
	
	
}
